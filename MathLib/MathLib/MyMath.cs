using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace MathLib
{
    public class MyMath
    {
        //phép cộng
        public int add(int a , int b)
        {
            return a + b;
        }

        //phép trừ
        public int sub(int a, int b)
        {
            return a - b;
        }

        //phép nhân
        public int mul(int a, int b)
        {
            return a * b;
        }

        //phép chia

        public int div(int a, int b)
        {
            return a / b;
        }

    }
